export function useLocationSharing(userInfo) {
  const [peers, setPeers] = useState([]);
  const [myLoc, setMyLocRaw] = useState(() => {
    try { return localStorage.getItem(LOC_KEY) || ""; } catch { return ""; }
  });
  const channelRef = useRef(null);
  const locRef = useRef(myLoc);

  const retrack = useCallback(() => {
    const ch = channelRef.current;
    if (!ch || !userInfo?.id) return;
    ch.track({
      id: userInfo.id,
      name: userInfo.name || '',
      col: userInfo.col || '#888',
      av: userInfo.av || '?',
      loc: locRef.current,
    });
  }, [userInfo?.id, userInfo?.name, userInfo?.col, userInfo?.av]);

  const setMyLoc = useCallback((loc) => {
    setMyLocRaw(loc);
    locRef.current = loc;
    try { localStorage.setItem(LOC_KEY, loc); } catch {}
    retrack();
  }, [retrack]);

  useEffect(() => {
    if (!userInfo?.id) return;

    let supabase;
    try { supabase = getSupabaseClient(); } catch { return; }
    if (!supabase) return;

    const channel = supabase.channel('presence:campus:location', {
      config: { presence: { key: String(userInfo.id) } },
    });

    const sync = () => {
      const state = channel.presenceState();
      const users = [];
      const seen = new Set();
      for (const [, entries] of Object.entries(state)) {
        for (const entry of entries) {
          if (!seen.has(entry.id) && entry.id !== userInfo.id && entry.loc) {
            seen.add(entry.id);
            users.push({ id: entry.id, name: entry.name, col: entry.col, av: entry.av, loc: entry.loc });
          }
        }
      }
      setPeers(users);
    };

    channel
      .on('presence', { event: 'sync' }, sync)
      .subscribe(async (status) => {
        if (status === 'SUBSCRIBED') {
          await channel.track({
            id: userInfo.id,
            name: userInfo.name || '',
            col: userInfo.col || '#888',
            av: userInfo.av || '?',
            loc: locRef.current,
          });
        }
      });

    channelRef.current = channel;

    return () => {
      channel.untrack();
      supabase.removeChannel(channel);
      channelRef.current = null;
    };
  }, [userInfo?.id]);

  return { peers, myLoc, setMyLoc };
}
